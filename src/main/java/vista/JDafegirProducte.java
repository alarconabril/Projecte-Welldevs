    private void afegirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_afegirButtonActionPerformed
        String codi = codeTF.getText();
        String nom = nameTF.getText();
        String descripcio = descTF.getText();
        double preu = (double) priceSpinner.getValue();
        int quantitat = (int) quantitySpinner.getValue();
        boolean totBe = true;
        if (codi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Has d'introdu√Ør un codi per el producte a afegir.", "ERROR", JOptionPane.ERROR_MESSAGE);
            totBe = false;}
        else if (codi.length() != 5) {
                JOptionPane.showMessageDialog(this, "El codi nom√©s te un total de 5 n√∫meros i lletres.", "ERROR", JOptionPane.ERROR_MESSAGE);
                totBe = false;}
                if (gestor.llistaProductes.containsKey(codi)) {
                  JOptionPane.showMessageDialog(this, "Aquest codi ja est√† registrat en altre producte.", "ERROR", JOptionPane.ERROR_MESSAGE);
                                totBe = false;}
                    if (nom.isEmpty()) {
                        JOptionPane.showMessageDialog(this, "El producte necesita un nom.", "ERROR", JOptionPane.ERROR_MESSAGE);
                        totBe = false;}
                        if (descripcio.isEmpty()) {
                            JOptionPane.showMessageDialog(this, "Has d'afegir una descripci√≥ al producte.", "ERROR", JOptionPane.ERROR_MESSAGE);
                            totBe = false; }
                         if (totBe) {
                             gestor.afegirProducte(codi, nom, descripcio, preu, quantitat);
                            JOptionPane.showMessageDialog(this, "El producte s'ha afegit correctament.", "√àxit!", JOptionPane.INFORMATION_MESSAGE);
                            dispose();
                        }
    }//GEN-LAST:event_afegirButtonActionPerformed

